public class DateFormat {
	/**
	 * 年月日 时分秒（24小时）
	 */
	public static final String STANDARD = "yyyy年MM月dd日 HH:mm:ss";
	/**
	 * 年月日 时分（24小时）
	 */
	public static final String STANDARD_NO_SEC = "yyyy年MM月dd日 HH:mm";
	/**
	 * 年月日
	 */
	public static final String STANDARD_NO_TIME = "yyyy年MM月dd日";

	/**
	 * 年月日 星期
	 */
	public static final String STANDARD_WEEK = "yyyy年MM月dd日 E";
	/**
	 * 年月日 上下午标识时分秒(12小时)
	 */
	public static final String STANDARD_DEC = "yyyy年MM月dd日 a hh:mm:ss";

}
